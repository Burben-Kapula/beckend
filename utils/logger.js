// Функція для виведення звичайних логів (інформація, успішні операції)
// ...params - rest оператор, приймає будь-яку кількість аргументів як масив
const info = (...params) => {
  // Розпаковуємо масив params і виводимо всі аргументи в консоль
  console.log(...params)
}

// Функція для виведення помилок (червоним кольором в консолі)
// Працює так само як info, але використовує console.error
const error = (...params) => {
  // Розпаковуємо і виводимо помилки (зазвичай показується червоним)
  console.error(...params)
}

// Експортуємо обидві функції для використання в інших файлах
// Замість console.log/error можна писати logger.info/error
module.exports = { info, error }
